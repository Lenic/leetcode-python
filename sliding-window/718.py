from typing import List


class Solution:
    def findLength(self, nums1: List[int], nums2: List[int]) -> int:
        def getCount(addNums1: int, addNums2: int, count: int) -> int:
            ans = k = 0
            for i in range(count):
                if nums1[addNums1 + i] == nums2[addNums2 + i]:
                    k += 1
                    ans = max(ans, k)
                else:
                    k = 0
            return ans

        ans: int = 0
        n, m = len(nums1), len(nums2)
        for i in range(1, n + 1):
            count = min(m, i)
            ans = max(ans, getCount(n - i, 0, count))
        for i in range(m):
            count = min(n, m - i)
            ans = max(ans, getCount(0, i, count))
        return ans


