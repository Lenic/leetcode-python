from typing import List

from collections import Counter


class Solution:
    def fourSumCount(self, nums1: List[int], nums2: List[int], nums3: List[int], nums4: List[int]) -> int:
        ans, dic = 0, Counter(x + y for x in nums1 for y in nums2)
        for x in nums3:
            for y in nums4:
                val = -x - y
                if val in dic:
                    ans += dic[val]
        return ans


